
#  What is tuple? Difference between list and tuple.

#  a tuple is a collection data type that is used to store an ordered sequence of elements, similar to a list. However, unlike lists, tuples are immutable—meaning that once a tuple is created, its elements cannot be modified, added, or removed.

# Defining a Tuple
# Tuples are created by placing elements inside parentheses () separated by commas.

# my_tuple = (1, 2, 3, "apple", "banana")

# Feature	                        List	                                      Tuple
# Mutability	          Mutable (elements can be changed).             Immutable (cannot be changed)
# Syntax	              Defined with square brackets [ ]	             Defined with parentheses ( )
# Performance	          Slower due to mutability.	                     Faster as they are fixed in size
# Memory Usage	          Uses more memory	                             Uses less memory
# Functions and           Supports many methods like                     Limited methods (count(), index())
# Methods                 append(), remove(), pop(), etc.	             Few methods like count, index.      
# Use Case	              Used for collections that 	                 Used for fixed collections that don’t 
