# How Many Basic Types Of Functions Are Available In Python?

# # there are four basic types of functions based on their purpose

# 1. Built-in Functions
#     These are predefined functions that come with Python. They are ready to use without any need for import or definition.
#     Examples: print(), len(), type(), int(), float(), input(), range(), max(), min(), etc.

# 2. User-Defined Functions
# These are functions created by the user to perform specific tasks. They are defined using the def keyword, followed by the function name and parameters (if any).
# structure:
# def function_name(parameters):
#     # code block
#     return value

# 3. Lambda Functions (Anonymous Functions)
# Lambda functions are small, anonymous functions defined using the lambda keyword. They are generally used for short, simple operations and are typically used as inline functions.
# Syntax: lambda arguments: expression
# Lambda functions can take multiple arguments but only have one expression.

# 4. Recursive Functions
# Recursive functions are functions that call themselves. They are commonly used to solve problems that can be broken down into smaller, similar problems (e.g., factorial, Fibonacci sequence).
# Recursive functions must have a base condition to prevent infinite recursion.
