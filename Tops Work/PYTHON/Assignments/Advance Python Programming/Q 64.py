#  Write a Python program to find the maximum and minimum numbers from the specified decimal numbers.


list = [1.5,3.1,2.65,0.4,0.9,0.5,5]
lis2 = sorted(list)
print(lis2)
print("maximum number from the specified decimal numbers list:",lis2[len(lis2)-1])
print("minimum number from the specified decimal numbers list:",lis2[0])




