
# Write a Python function to get the largest number, smallest num and sum of all from a list.  


def lrg_num(xy):
    return max(xy)

def sml_num(xy):
    return min(xy)

def sum(xy):
    return sum(xy)

abc = [1,2,9,8,7,-6,-2]
print(f"Largest number in a list: {lrg_num(abc)}")
print(f"Smallest number in a list: {sml_num(abc)}")
print(f"Sum of all numbers in a list: {sum(abc)}")
